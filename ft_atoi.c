#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	int j;
	int tot;

	i = 0;
	j = 0;
	tot = 0;
	while ((str[i] >= '0' && str[i] <= '9') || str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j++;
		else if (str[i] != '+')
		{
			tot *= 10;
			tot += str[i] - 48;
		}
		i++;
	}
	if (j % 2)
		tot *= -1;
	return (tot);
}
