#include "libft.h"

int		ft_strcmp(char *st1, char *st2)
{
	char *s1;
	char *s2;

	s1 = st1;
	s2 = st2;
	while ((*s1 && *s2) && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	if ((*s1 && !*s2) || *s1 > *s2)
		return (1);
	else if ((*s2 && !*s1) || *s2 > *s1)
		return (-1);
	else
		return (0);
}
